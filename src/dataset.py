"""
dataset.py
──────────
PyTorch Dataset for FMCG product images.

Supports:
  • Loading from CSV annotations (generated by annotation_tool.py)
  • Loading from folder structure (class_name/image.jpg)
  • Train / val / test split with stratification
  • Integration with the augmentation pipeline
"""

import csv
from pathlib import Path
from typing import Callable, List, Optional, Tuple

import numpy as np
import torch
from PIL import Image
from sklearn.model_selection import train_test_split
from torch.utils.data import Dataset, DataLoader

from augmentation import build_train_transforms, build_val_transforms, to_tensor


class FMCGDataset(Dataset):
    """Dataset of FMCG product images with optional augmentation."""

    def __init__(
        self,
        image_paths: List[Path],
        labels: List[int],
        class_names: List[str],
        transform=None,
    ):
        self.image_paths = image_paths
        self.labels = labels
        self.class_names = class_names
        self.transform = transform

    def __len__(self) -> int:
        return len(self.image_paths)

    def __getitem__(self, idx: int) -> Tuple[torch.Tensor, int]:
        img = Image.open(self.image_paths[idx]).convert("RGB")
        img_np = np.array(img)

        if self.transform:
            augmented = self.transform(image=img_np)
            img_np = augmented["image"]

        tensor = to_tensor(img_np)
        return tensor, self.labels[idx]


# ── Factory function ───────────────────────────────────────────────────────────

def build_datasets(
    annotations_csv: str,
    image_size: int = 224,
    test_split: float = 0.15,
    val_split: float = 0.10,
    random_seed: int = 42,
) -> Tuple[FMCGDataset, FMCGDataset, FMCGDataset, List[str]]:
    """
    Build train / val / test datasets from an annotations CSV.

    CSV format:
        image_path, label, clip_confidence, annotation_method

    Returns:
        (train_dataset, val_dataset, test_dataset, class_names)
    """
    rows = []
    with open(annotations_csv) as f:
        reader = csv.DictReader(f)
        for row in reader:
            rows.append(row)

    image_paths = [Path(r["image_path"]) for r in rows]
    str_labels = [r["label"] for r in rows]

    # Encode string labels to integers
    class_names = sorted(set(str_labels))
    label_to_idx = {cls: i for i, cls in enumerate(class_names)}
    int_labels = [label_to_idx[l] for l in str_labels]

    # Stratified split
    indices = list(range(len(image_paths)))
    train_val_idx, test_idx = train_test_split(
        indices, test_size=test_split, stratify=int_labels, random_state=random_seed
    )
    train_val_labels = [int_labels[i] for i in train_val_idx]
    val_fraction = val_split / (1.0 - test_split)
    train_idx, val_idx = train_test_split(
        train_val_idx,
        test_size=val_fraction,
        stratify=train_val_labels,
        random_state=random_seed,
    )

    def subset(indices, transform):
        return FMCGDataset(
            image_paths=[image_paths[i] for i in indices],
            labels=[int_labels[i] for i in indices],
            class_names=class_names,
            transform=transform,
        )

    train_tf = build_train_transforms(image_size, strong=True)
    val_tf = build_val_transforms(image_size)

    return (
        subset(train_idx, train_tf),
        subset(val_idx, val_tf),
        subset(test_idx, val_tf),
        class_names,
    )


def build_dataloaders(
    train_ds: FMCGDataset,
    val_ds: FMCGDataset,
    test_ds: FMCGDataset,
    batch_size: int = 32,
    num_workers: int = 4,
) -> Tuple[DataLoader, DataLoader, DataLoader]:
    """Build DataLoaders for train / val / test."""
    train_loader = DataLoader(
        train_ds,
        batch_size=batch_size,
        shuffle=True,
        num_workers=num_workers,
        pin_memory=True,
        drop_last=True,
    )
    val_loader = DataLoader(
        val_ds,
        batch_size=batch_size,
        shuffle=False,
        num_workers=num_workers,
        pin_memory=True,
    )
    test_loader = DataLoader(
        test_ds,
        batch_size=batch_size,
        shuffle=False,
        num_workers=num_workers,
        pin_memory=True,
    )
    return train_loader, val_loader, test_loader
